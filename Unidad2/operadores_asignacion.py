#Operadores de Asignacion

# definicion de una variable
variable_1 = 20

#Suma y Asigna
variable_1 += 5
print(type(variable_1))
print(variable_1) #25

#Resta y Asigna
variable_1 -= 10
print(type(variable_1))
print(variable_1)  #15

#Multiplica y Asigna
variable_1 *= 10
print(type(variable_1))
print(variable_1)  #150

#Divide y Asigna (Operacion basica)
variable_1 /= 10
print(type(variable_1))
print(variable_1)  # float(15.0)

variable_2 = 110
variable_2 //= 10 #(Operacion de tipo de dato Entero)
print(type(variable_2))
print(variable_2)  # int

#Resto
variable_2 = 6
variable_2 %= 2 #(Operacion de tipo de dato Entero)
print(type(variable_2))
print(variable_2)  # int


#Potencia
variable_3 = 5
variable_3 **= 2 #(Operacion de tipo de dato Entero)

print(type(variable_3))
print(variable_3)  # int

